def validate_html_structure(html_text: str) -> dict[str, Any]:
    report_section_count = html_text.count('<section class="section">')
    total_section_open_count = len(re.findall(r"<section(?:\s|>)", html_text))
    section_close_count = html_text.count("</section>")
    h2_count = html_text.count("<h2>")
    valid = (
        report_section_count == 14
        and h2_count == 14
        and total_section_open_count == section_close_count
    )
    return {
        "html_structure_valid": valid,
        "report_section_count": report_section_count,
        "total_section_open_count": total_section_open_count,
        "section_close_count": section_close_count,
        "h2_count": h2_count,
    }


def output_path(prefix: Path, suffix: str) -> Path:
    return prefix.parent / f"{prefix.name}{suffix}"


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--source-md", required=True, type=Path)
    parser.add_argument("--output-prefix", required=True, type=Path)
    parser.add_argument("--company-json", type=Path)
    args = parser.parse_args()

    source = args.source_md.read_text(encoding="utf-8")
    meta = infer_company_meta(args.source_md, source, args.company_json)
    _, sections = split_sections(source)
    upgraded = build_markdown(source, sections, meta)
    evidence_lines = extract_evidence_lines(upgraded)
    report_json = build_json(upgraded, evidence_lines, meta)
    html_text = render_html(upgraded, meta)
    html_check = validate_html_structure(html_text)
    report_json["quality_report"]["html_structure"] = html_check
    if not html_check["html_structure_valid"]:
        raise RuntimeError(f"HTML structure validation failed: {html_check}")

    output_path(args.output_prefix, ".md").write_text(upgraded, encoding="utf-8")
    output_path(args.output_prefix, ".html").write_text(html_text, encoding="utf-8")
    with output_path(args.output_prefix, ".json").open("w", encoding="utf-8") as fh:
        json.dump(report_json, fh, ensure_ascii=False, indent=2)
        fh.write("\n")
    print(json.dumps({
        "ok": True,
        "output_prefix": str(args.output_prefix),
        "sections": len(report_json["sections"]),
        "evidence_lines": len(evidence_lines),
        "md_lines": len(upgraded.splitlines()),
    }, ensure_ascii=False))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
