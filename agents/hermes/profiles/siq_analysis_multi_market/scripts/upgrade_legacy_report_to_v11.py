def inline_markdown(text: str) -> str:
    text = html.escape(text)
    text = re.sub(r"\*\*(.+?)\*\*", r"<strong>\1</strong>", text)
    text = re.sub(r"\[([^\[\]]+?)\]\(([^()\s]+?)\)", r'<a href="\2">\1</a>', text)
    return text


def markdown_to_html(markdown: str) -> str:
    lines = markdown.splitlines()
    parts: list[str] = []
    in_table = False
    in_ul = False
    in_quote = False
    in_code = False
    in_section = False
    code_lines: list[str] = []

    def close_table() -> None:
        nonlocal in_table
        if in_table:
            parts.append("</tbody></table>")
            in_table = False

    def close_ul() -> None:
        nonlocal in_ul
        if in_ul:
            parts.append("</ul>")
            in_ul = False

    def close_quote() -> None:
        nonlocal in_quote
        if in_quote:
            parts.append("</blockquote>")
            in_quote = False

    def close_section() -> None:
        nonlocal in_section
        close_table()
        close_ul()
        close_quote()
        if in_section:
            parts.append("</section>")
            in_section = False

    for raw in lines:
        line = raw.rstrip()
        if line.startswith("```"):
            if in_code:
                parts.append("<pre><code>" + html.escape("\n".join(code_lines)) + "</code></pre>")
                code_lines = []
                in_code = False
            else:
                close_table(); close_ul(); close_quote()
                in_code = True
            continue
        if in_code:
            code_lines.append(line)
            continue
        if not line.strip():
            close_table(); close_ul(); close_quote()
            continue
        if line.startswith("# "):
            close_section()
            parts.append(f"<h1>{inline_markdown(line[2:])}</h1>")
        elif line.startswith("## "):
            close_section()
            parts.append(f"<section class=\"section\"><h2>{inline_markdown(line[3:])}</h2>")
            in_section = True
        elif line.startswith("### "):
            close_table(); close_ul(); close_quote()
            parts.append(f"<h3>{inline_markdown(line[4:])}</h3>")
        elif line.startswith(">"):
            close_table(); close_ul()
            if not in_quote:
                parts.append("<blockquote>")
                in_quote = True
            parts.append(f"<p>{inline_markdown(line.lstrip('> ').strip())}</p>")
        elif line.startswith("|") and line.endswith("|"):
            close_ul(); close_quote()
            cells = [inline_markdown(cell.strip()) for cell in line.strip("|").split("|")]
            if len(cells) >= 2 and all(set(cell.replace(":", "").replace("-", "").strip()) == set() for cell in cells):
                continue
            if not in_table:
                parts.append("<table><tbody>")
                in_table = True
            tag = "th" if all("<strong>" not in cell for cell in cells) and ("指标" in raw or "项目" in raw or "来源" in raw or "跟踪" in raw) else "td"
            parts.append("<tr>" + "".join(f"<{tag}>{cell}</{tag}>" for cell in cells) + "</tr>")
        elif line.startswith("- "):
            close_table(); close_quote()
            if not in_ul:
                parts.append("<ul>")
                in_ul = True
            parts.append(f"<li>{inline_markdown(line[2:])}</li>")
        else:
            close_table(); close_ul(); close_quote()
            if line == "---":
                parts.append("<hr>")
            else:
                parts.append(f"<p>{inline_markdown(line)}</p>")
    close_section()
    return "\n".join(parts)


def number_from_text(value: str) -> float | None:
    cleaned = re.sub(r"[^\d.\-]", "", value or "")
    try:
        return float(cleaned)
    except ValueError:
        return None


