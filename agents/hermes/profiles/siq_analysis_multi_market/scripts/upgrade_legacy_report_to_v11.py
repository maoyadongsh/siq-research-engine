def split_sections(markdown: str) -> tuple[str, dict[str, str]]:
    matches = list(re.finditer(r"^##\s+(.+)$", markdown, flags=re.M))
    preface = markdown[: matches[0].start()].strip() if matches else markdown.strip()
    sections: dict[str, str] = {}
    for idx, match in enumerate(matches):
        title = match.group(1).strip()
        end = matches[idx + 1].start() if idx + 1 < len(matches) else len(markdown)
        body = markdown[match.end() : end].strip()
        sections[title] = body
    return preface, sections


def normalize_heading_levels(text: str) -> str:
    lines = []
    for line in text.splitlines():
        if line.startswith("### "):
            lines.append("### " + line[4:])
        elif line.startswith("#### "):
            lines.append("#### " + line[5:])
        else:
            lines.append(line)
    return "\n".join(lines).strip()


def first_paragraph(section_body: str) -> str:
    for block in re.split(r"\n\s*\n", section_body.strip()):
        text = re.sub(r"\s+", " ", re.sub(r"[*_`>#|-]", "", block)).strip()
        if text and not text.startswith("|"):
            return text[:240]
    return ""


def infer_company_meta(source_md: Path, markdown: str, company_json: Path | None = None) -> dict[str, Any]:
    meta = dict(DEFAULT_COMPANY_META)
    if company_json and company_json.exists():
        try:
            data = json.loads(company_json.read_text(encoding="utf-8"))
            meta["company_dir"] = str(company_json.parent)
            meta.update({
                "company_id": data.get("company_id") or meta["company_id"],
                "stock_code": data.get("stock_code") or meta["stock_code"],
                "company_short_name": data.get("company_short_name") or meta["company_short_name"],
                "company_full_name": data.get("company_full_name") or meta["company_full_name"],
                "exchange": data.get("exchange"),
            })
            reports = data.get("reports") or []
            if reports:
                meta["task_id"] = reports[0].get("task_id") or meta.get("task_id", "")
                meta["source_filename"] = reports[0].get("source_filename")
        except (OSError, json.JSONDecodeError):
            pass

    if not company_json:
        parts = source_md.parts
        if "companies" in parts:
            idx = parts.index("companies")
            if idx + 1 < len(parts):
                company_id = parts[idx + 1]
                meta["company_id"] = company_id
                if "-" in company_id:
                    code, name = company_id.split("-", 1)
                    meta["stock_code"] = code
                    meta["company_short_name"] = name

    title_match = re.search(r"^#\s+(.+?)[（(](\d{6})[）)]", markdown, flags=re.M)
    if title_match:
        meta["company_short_name"] = title_match.group(1).strip()
        meta["stock_code"] = title_match.group(2)
        meta["company_id"] = f"{meta['stock_code']}-{meta['company_short_name']}"
    task_match = re.search(r"task_id[*\s：:]*`?([0-9a-fA-F-]{24,})`?", markdown)
    if task_match:
        meta["task_id"] = task_match.group(1)
    return meta


def metric_rows(markdown: str) -> dict[str, dict[str, str]]:
    rows: dict[str, dict[str, str]] = {}
    header: list[str] | None = None
    for line in markdown.splitlines():
        if not line.startswith("|") or "---" in line:
            if line.startswith("|") and "---" in line:
                continue
            header = None
            continue
        cells = [c.strip().strip("*") for c in line.strip("|").split("|")]
        joined = " ".join(cells)
        if ".json" in joined or "task_id" in joined or "pdf_page" in joined or "table_index" in joined:
            continue
        if not header:
            header = cells
            continue
        if len(cells) < 2:
            continue
        metric_name = cells[0]
        if metric_name in rows:
            continue
        mapped = {"2023": "", "2024": "", "2025": "", "change": ""}
        for idx, title in enumerate(header[1:], start=1):
            if idx >= len(cells):
                continue
            if "2025" in title:
                mapped["2025"] = cells[idx]
            elif "2024" in title:
                mapped["2024"] = cells[idx]
            elif "2023" in title:
                mapped["2023"] = cells[idx]
            elif "变化" in title or "同比" in title:
                mapped["change"] = cells[idx]
        if any(mapped.values()):
            rows[metric_name] = mapped
    return rows


def row_value(rows: dict[str, dict[str, str]], key: str, field: str = "2025", default: str = "未返回") -> str:
    return rows.get(key, {}).get(field) or default


def first_available(rows: dict[str, dict[str, str]], keys: list[str], field: str = "2025") -> str:
    for key in keys:
        value = row_value(rows, key, field, default="")
        if value:
            return value
    return "未返回"


def fmt_metric_amount(value: float | int | None, *, percent: bool = False) -> str:
    if value is None:
        return "未返回"
    if percent:
        return f"{value:.2f}%"
    return f"{value / 1e8:.2f}亿元"


def yoy_text(current: float | int | None, previous: float | int | None, *, loss_word: bool = False) -> str:
    if current is None or previous in (None, 0):
        return "未返回"
    change = (float(current) - float(previous)) / abs(float(previous)) * 100
    if loss_word and current < 0 and previous < 0:
        return f"亏损{'收窄' if change > 0 else '扩大'} {abs(change):.2f}%"
    return f"{change:+.2f}%"


def load_key_metric_rows(meta: dict[str, Any]) -> dict[str, dict[str, str]]:
    company_dir = meta.get("company_dir")
    if not company_dir:
        return {}
    path = Path(company_dir) / "metrics" / "key_metrics.json"
    if not path.exists():
        return {}
    try:
        items = json.loads(path.read_text(encoding="utf-8")).get("data") or []
    except (OSError, json.JSONDecodeError):
        return {}
    by_name = {item.get("canonical_name"): item for item in items if isinstance(item, dict)}

    def vals(canonical: str) -> dict[str, Any]:
        raw = by_name.get(canonical, {}).get("values") or {}
        return {year: raw.get(year) for year in ("2023", "2024", "2025")}

    revenue = vals("operating_revenue")
    parent_profit = vals("parent_net_profit")
    deducted_profit = vals("deducted_parent_net_profit")
    ocf = vals("operating_cash_flow_net")
    assets = vals("total_assets")
    equity = vals("equity_attributable_parent")
    return {
        "营业收入": {
            year: fmt_metric_amount(revenue.get(year)) for year in ("2023", "2024", "2025")
        } | {"change": yoy_text(revenue.get("2025"), revenue.get("2024"))},
        "归母净利润": {
            year: fmt_metric_amount(parent_profit.get(year)) for year in ("2023", "2024", "2025")
        } | {"change": yoy_text(parent_profit.get("2025"), parent_profit.get("2024"), loss_word=True)},
        "扣非归母净利润": {
            year: fmt_metric_amount(deducted_profit.get(year)) for year in ("2023", "2024", "2025")
        } | {"change": yoy_text(deducted_profit.get("2025"), deducted_profit.get("2024"), loss_word=True)},
        "经营现金流": {
            year: fmt_metric_amount(ocf.get(year)) for year in ("2023", "2024", "2025")
        } | {"change": yoy_text(ocf.get("2025"), ocf.get("2024"))},
        "总资产": {
            year: fmt_metric_amount(assets.get(year)) for year in ("2023", "2024", "2025")
        } | {"change": yoy_text(assets.get("2025"), assets.get("2024"))},
        "归母净资产": {
            year: fmt_metric_amount(equity.get(year)) for year in ("2023", "2024", "2025")
        } | {"change": yoy_text(equity.get("2025"), equity.get("2024"))},
    }


